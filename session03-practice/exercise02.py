@app.get("/books/available")
def get_books():
    book_available = []
    for book in books:
        if book["is_available"] == True:
            book_available.append(book)
    return book_available

@app.get("/books/borrowed")
def get_books():
    book_borrowed = []
    for book in books:
        if book["is_available"] == False:
            book_borrowed.append(book)
    return book_borrowed

